USE injupen_db;

SET NAMES utf8mb4;

CREATE TABLE IF NOT EXISTS tipos_propiedad (
  ID_TIPO_PROPIEDAD INT NOT NULL AUTO_INCREMENT,
  NOMBRE VARCHAR(120) NOT NULL,
  SLUG VARCHAR(140) NOT NULL,
  DESCRIPCION TEXT NULL,
  ESTADO TINYINT(1) NOT NULL DEFAULT 1,
  FECHA_CREACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FECHA_ACTUALIZACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (ID_TIPO_PROPIEDAD),
  UNIQUE KEY UK_TIPOS_PROPIEDAD_NOMBRE (NOMBRE),
  UNIQUE KEY UK_TIPOS_PROPIEDAD_SLUG (SLUG)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS zonas (
  ID_ZONA INT NOT NULL AUTO_INCREMENT,
  NOMBRE VARCHAR(150) NOT NULL,
  CIUDAD VARCHAR(150) NOT NULL,
  SLUG VARCHAR(180) NOT NULL,
  DESCRIPCION TEXT NULL,
  ESTADO TINYINT(1) NOT NULL DEFAULT 1,
  FECHA_CREACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FECHA_ACTUALIZACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (ID_ZONA),
  UNIQUE KEY UK_ZONAS_SLUG (SLUG),
  KEY IDX_ZONAS_CIUDAD (CIUDAD),
  KEY IDX_ZONAS_ESTADO (ESTADO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS agentes (
  ID_AGENTE INT NOT NULL AUTO_INCREMENT,
  ID_USUARIO INT NULL,
  NOMBRE VARCHAR(180) NOT NULL,
  CARGO VARCHAR(150) NOT NULL,
  TELEFONO VARCHAR(40) NOT NULL,
  CORREO VARCHAR(180) NOT NULL,
  FOTO_URL VARCHAR(500) NULL,
  ESPECIALIDAD VARCHAR(180) NULL,
  ESTADO ENUM('Activo', 'Inactivo') NOT NULL DEFAULT 'Activo',
  FECHA_CREACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FECHA_ACTUALIZACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (ID_AGENTE),
  UNIQUE KEY UK_AGENTES_CORREO (CORREO),
  KEY IDX_AGENTES_ESTADO (ESTADO),
  KEY IDX_AGENTES_USUARIO (ID_USUARIO),
  CONSTRAINT FK_AGENTES_USUARIO
    FOREIGN KEY (ID_USUARIO) REFERENCES gestion_usuarios (CODIGO)
    ON DELETE SET NULL
    ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS propiedades (
  ID_PROPIEDAD INT NOT NULL AUTO_INCREMENT,
  ID_TIPO_PROPIEDAD INT NOT NULL,
  ID_ZONA INT NOT NULL,
  ID_AGENTE INT NOT NULL,
  TITULO VARCHAR(220) NOT NULL,
  SLUG VARCHAR(260) NOT NULL,
  OPERACION ENUM('Venta', 'Renta') NOT NULL,
  PRECIO DECIMAL(14,2) NOT NULL DEFAULT 0.00,
  HABITACIONES INT NOT NULL DEFAULT 0,
  BANOS DECIMAL(4,1) NOT NULL DEFAULT 0.0,
  ESTACIONAMIENTOS INT NOT NULL DEFAULT 0,
  AREA_M2 DECIMAL(12,2) NOT NULL DEFAULT 0.00,
  DIRECCION VARCHAR(255) NOT NULL,
  DESCRIPCION TEXT NOT NULL,
  IMAGEN_PORTADA LONGTEXT NULL,
  DESTACADA TINYINT(1) NOT NULL DEFAULT 0,
  ACTIVA TINYINT(1) NOT NULL DEFAULT 1,
  ESTADO_COMERCIAL ENUM('Disponible', 'Vendida', 'Rentada', 'Inactiva') NOT NULL DEFAULT 'Disponible',
  ESTADO_PUBLICACION ENUM('Borrador', 'Pendiente de pago', 'Publicada', 'Vencida', 'Pausada') NOT NULL DEFAULT 'Borrador',
  FECHA_PUBLICACION TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP,
  FECHA_CREACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FECHA_ACTUALIZACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (ID_PROPIEDAD),
  UNIQUE KEY UK_PROPIEDADES_SLUG (SLUG),
  KEY IDX_PROPIEDADES_TIPO (ID_TIPO_PROPIEDAD),
  KEY IDX_PROPIEDADES_ZONA (ID_ZONA),
  KEY IDX_PROPIEDADES_AGENTE (ID_AGENTE),
  KEY IDX_PROPIEDADES_OPERACION (OPERACION),
  KEY IDX_PROPIEDADES_DESTACADA (DESTACADA),
  KEY IDX_PROPIEDADES_ACTIVA (ACTIVA),
  KEY IDX_PROPIEDADES_ESTADO_COMERCIAL (ESTADO_COMERCIAL),
  KEY IDX_PROPIEDADES_ESTADO_PUBLICACION (ESTADO_PUBLICACION),
  CONSTRAINT FK_PROPIEDADES_TIPO
    FOREIGN KEY (ID_TIPO_PROPIEDAD) REFERENCES tipos_propiedad (ID_TIPO_PROPIEDAD)
    ON DELETE RESTRICT
    ON UPDATE CASCADE,
  CONSTRAINT FK_PROPIEDADES_ZONA
    FOREIGN KEY (ID_ZONA) REFERENCES zonas (ID_ZONA)
    ON DELETE RESTRICT
    ON UPDATE CASCADE,
  CONSTRAINT FK_PROPIEDADES_AGENTE
    FOREIGN KEY (ID_AGENTE) REFERENCES agentes (ID_AGENTE)
    ON DELETE RESTRICT
    ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS propiedad_imagenes (
  ID_IMAGEN INT NOT NULL AUTO_INCREMENT,
  ID_PROPIEDAD INT NOT NULL,
  URL_IMAGEN LONGTEXT NOT NULL,
  TEXTO_ALTERNATIVO VARCHAR(255) NULL,
  ORDEN_VISUAL INT NOT NULL DEFAULT 1,
  ES_PORTADA TINYINT(1) NOT NULL DEFAULT 0,
  FECHA_CREACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (ID_IMAGEN),
  KEY IDX_IMAGENES_PROPIEDAD (ID_PROPIEDAD),
  KEY IDX_IMAGENES_ORDEN (ID_PROPIEDAD, ORDEN_VISUAL),
  CONSTRAINT FK_IMAGENES_PROPIEDAD
    FOREIGN KEY (ID_PROPIEDAD) REFERENCES propiedades (ID_PROPIEDAD)
    ON DELETE CASCADE
    ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

