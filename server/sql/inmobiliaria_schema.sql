CREATE TABLE IF NOT EXISTS suscripciones_publicacion (
  ID_SUSCRIPCION INT NOT NULL AUTO_INCREMENT,
  ID_PROPIEDAD INT NOT NULL,
  ID_PLAN INT NOT NULL,
  ID_AGENTE INT NULL,
  PRECIO_PLAN DECIMAL(12,2) NOT NULL DEFAULT 0.00,
  PRECIO_FINAL DECIMAL(12,2) NOT NULL DEFAULT 0.00,
  FECHA_INICIO DATE NOT NULL,
  FECHA_FIN DATE NOT NULL,
  ESTADO_SUSCRIPCION ENUM('Pendiente de pago', 'Activa', 'Vencida', 'Cancelada') NOT NULL DEFAULT 'Pendiente de pago',
  AUTO_RENOVAR TINYINT(1) NOT NULL DEFAULT 0,
  OBSERVACIONES TEXT NULL,
  FECHA_CREACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FECHA_ACTUALIZACION TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (ID_SUSCRIPCION),
  KEY IDX_SUSCRIPCIONES_PROPIEDAD (ID_PROPIEDAD),
  KEY IDX_SUSCRIPCIONES_PLAN (ID_PLAN),
  KEY IDX_SUSCRIPCIONES_AGENTE (ID_AGENTE),
  KEY IDX_SUSCRIPCIONES_ESTADO (ESTADO_SUSCRIPCION),
  KEY IDX_SUSCRIPCIONES_FECHAS (FECHA_INICIO, FECHA_FIN),
  CONSTRAINT FK_SUSCRIPCIONES_PROPIEDAD
    FOREIGN KEY (ID_PROPIEDAD) REFERENCES propiedades (ID_PROPIEDAD)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
  CONSTRAINT FK_SUSCRIPCIONES_PLAN
    FOREIGN KEY (ID_PLAN) REFERENCES planes_publicacion (ID_PLAN)
    ON DELETE RESTRICT
    ON UPDATE CASCADE,
  CONSTRAINT FK_SUSCRIPCIONES_AGENTE
    FOREIGN KEY (ID_AGENTE) REFERENCES agentes (ID_AGENTE)
    ON DELETE SET NULL
    ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

